#!/usr/bin/env python3
"""Register XPolicyLab 5-tuple LeRobot data and emit Being-H posttrain yaml."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


SCRIPT_DIR = Path(__file__).resolve().parent
POLICY_DIR = SCRIPT_DIR.parent
BEINGH_DIR = POLICY_DIR / "Being-H"
OVERRIDE_JSON = BEINGH_DIR / "configs" / "dataset_info_xpolicylab.json"
GENERATED_DIR = BEINGH_DIR / "configs" / "posttrain" / "xpolicylab"

ACTION_TO_DATA_CONFIG = {
    "joint": "robodojo_qpos",
}


def register_dataset(data_tag: str, data_path: str, registry: str = "robodojo_posttrain") -> None:
    data_path = str(Path(data_path).resolve())
    overrides: dict = {}
    if OVERRIDE_JSON.exists():
        overrides = json.loads(OVERRIDE_JSON.read_text(encoding="utf-8"))
    overrides.setdefault(registry, {})[data_tag] = {"dataset_path": data_path}
    OVERRIDE_JSON.parent.mkdir(parents=True, exist_ok=True)
    OVERRIDE_JSON.write_text(json.dumps(overrides, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"[xpolicylab_dataset] registered {registry}/{data_tag} -> {data_path}")


def write_train_yaml(
    data_tag: str,
    expert_data_num: int,
    action_type: str,
    output_path: Path | None = None,
) -> Path:
    if action_type not in ACTION_TO_DATA_CONFIG:
        raise ValueError(f"unsupported action_type={action_type!r}, expected one of {sorted(ACTION_TO_DATA_CONFIG)}")

    data_config_name = ACTION_TO_DATA_CONFIG[action_type]
    num_episodes = int(expert_data_num) if int(expert_data_num) > 0 else -1

    content = f"""# Auto-generated by policy/Being_H05/process_data.sh (XPolicyLab 5-tuple: {data_tag})

robodojo_posttrain:
  dataset_names:
  - {data_tag}

  data_config_names:
  - "{data_config_name}"

  embodiment_tags:
  - "new_embodiment"

  sampling_strategy: "step"
  video_backend: "torchvision_av"

  vit_transform_args:
    type: "beingh"
    normalize_type: "imagenet"
    use_color_jitter: true

  frame_step_size:
  - 1

  num_used_episodes_per_task:
  - -1

  num_used_episodes_per_dataset:
  - {num_episodes}

  num_used_frames_per_dataset:
  - -1

  weight: 1
"""

    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    out_path = output_path or (GENERATED_DIR / f"{data_tag}.yaml")
    out_path.write_text(content, encoding="utf-8")
    print(f"[xpolicylab_dataset] wrote train yaml: {out_path}")
    return out_path


def main() -> None:
    parser = argparse.ArgumentParser()
    sub = parser.add_subparsers(dest="cmd", required=True)

    reg = sub.add_parser("register")
    reg.add_argument("--data-tag", required=True)
    reg.add_argument("--data-path", required=True)

    gen = sub.add_parser("write-yaml")
    gen.add_argument("--data-tag", required=True)
    gen.add_argument("--expert-data-num", type=int, required=True)
    gen.add_argument("--action-type", required=True)
    gen.add_argument("--output", default=None)

    prep = sub.add_parser("prepare")
    prep.add_argument("--data-tag", required=True)
    prep.add_argument("--data-path", required=True)
    prep.add_argument("--expert-data-num", type=int, required=True)
    prep.add_argument("--action-type", required=True)

    args = parser.parse_args()
    if args.cmd == "register":
        register_dataset(args.data_tag, args.data_path)
    elif args.cmd == "write-yaml":
        write_train_yaml(args.data_tag, args.expert_data_num, args.action_type, Path(args.output) if args.output else None)
    elif args.cmd == "prepare":
        register_dataset(args.data_tag, args.data_path)
        write_train_yaml(args.data_tag, args.expert_data_num, args.action_type)


if __name__ == "__main__":
    main()
